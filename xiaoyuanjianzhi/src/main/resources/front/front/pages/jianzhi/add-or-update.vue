            async onSubmitTap() {
				if ((!this.ruleForm.jianzhiName)) {
                    this.$utils.msg(`兼职名称不能为空`);
                    return
                }
				if ((!this.ruleForm.jianzhiUuidNumber)) {
                    this.$utils.msg(`兼职编号不能为空`);
                    return
                }
				if ((!this.ruleForm.jianzhiPhoto)) {
                    this.$utils.msg(`兼职照片不能为空`);
                    return
                }
				if ((!this.ruleForm.zanNumber) && (!this.$validate.isIntNumer(this.ruleForm.zanNumber)) && this.ruleForm.zanNumber >0) {
                    this.$utils.msg(`赞不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.caiNumber) && (!this.$validate.isIntNumer(this.ruleForm.caiNumber)) && this.ruleForm.caiNumber >0) {
                    this.$utils.msg(`踩不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.jianzhiJiesuanTypes)) {
                    this.$utils.msg(`结算类型不能为空`);
                    return
                }
				if ((!this.ruleForm.jianzhiKucunNumber) && (!this.$validate.isIntNumer(this.ruleForm.jianzhiKucunNumber)) && this.ruleForm.jianzhiKucunNumber >0) {
                    this.$utils.msg(`招聘人数不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.jianzhiPhone) && (!this.$validate.isMobile(this.ruleForm.jianzhiPhone))) {
                    this.$utils.msg(`联系方式不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.jianzhiAddress)) {
                    this.$utils.msg(`工作地点不能为空`);
                    return
                }
				if ((!this.ruleForm.jianzhiShijian)) {
                    this.$utils.msg(`工作时间不能为空`);
                    return
                }
				if ((!this.ruleForm.jianzhiDaiyu)) {
                    this.$utils.msg(`薪资/小时不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`jianzhi`, this.ruleForm);
                } else {
                    await this.$api.save(`jianzhi`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
