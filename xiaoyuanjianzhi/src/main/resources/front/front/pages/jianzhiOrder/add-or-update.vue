            user: {},
				jianzhiOrderTypesOptions: [],
							jianzhiOrderTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {

		/*下拉框*/
			let jianzhiOrderTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'jianzhi_order_types',
            }
			let jianzhiOrderTypes = await this.$api.page(`dictionary`, jianzhiOrderTypesParams);
			this.jianzhiOrderTypesOptions = jianzhiOrderTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`jianzhiOrder`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.jianzhiOrderId){
                this.ruleForm.jianzhiOrderId = options.jianzhiOrderId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            jianzhiOrderTypesChange(e) {
                this.jianzhiOrderTypesIndex = e.target.value
                this.ruleForm.jianzhiOrderValue = this.jianzhiOrderTypesOptions[this.jianzhiOrderTypesIndex].indexName
                this.ruleForm.jianzhiOrderTypes = this.jianzhiOrderTypesOptions[this.jianzhiOrderTypesIndex].codeIndex
            },


            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
